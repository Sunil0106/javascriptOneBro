// = assingment operators
// == comparison operators (compare if the value are equal)
// === strict equality operators (compare if value and datatype are equal)
// != inequality operator
// !== strict inequality operators
const PI = 3.14;
if (PI == "3.14") {
  console.log("That is pi");
} else {
  console.log("That is not pi");
}
if (PI === "3.14") {
  console.log("That is pi");
} else {
  console.log("That is not pi");
}
if (PI != "3.14") {
  console.log("That is not pi");
} else {
  console.log("That is pi");
}
if (PI !== "3.14") {
  console.log("That is not pi");
} else {
  console.log("That is pi");
}
